fun main(){
    // A variable is a container that stores data/values
    //In kotlin, we have to main types of initializers i.e: var and val
    //Val=> Immutable (unchangable),var=> Mutable (changable) 

    val name = "Tabby Ondego" //String -> a series of characters inside of quotes
    val number1 = 5 // Integer -> A whole number
    val number2 = 40.6 // Double -> Number with a decimal
    val letter = "k" // Char -> A single letter of the alphabet
    val isRegistered = true //Boolean -> It evaluates whether true or false

    println(name)
    println(number1)
    println(number2)
    println(letter)
    println(isRegistered)
}