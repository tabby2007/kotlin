fun main() {
    //if...else if...else conditional statements
    //This is used to evaluate mutiple conditions. The else part get executed when all the conditions given evaluate to false

    val number = 9

    if(number > 0){
        println("The number is positive.")
    }
    else if (number == 0){
        println("The number is zero.")
    }
    else{
        println("The number is negative.")
    }
}