// Conditional statements in kotlin
// if statement
fun main() {
    val number = 10

    if (number > 0) {
        println("Positive number")
    }
}

// if...else
fun main() {
    val number = -5

    if (number > 0) {
        println("Positive")
    } else {
        println("Negative")
    }
}

